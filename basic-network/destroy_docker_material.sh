echo " "
echo " "


echo "----------------------------------------------------------------------------"
echo "          DELETING DOCKER NETWORKS"
echo "----------------------------------------------------------------------------"

docker network prune

echo "!!!!!!!!!!      DONE!         !!!!!!!!!!!!"
